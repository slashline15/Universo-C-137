cd caminho/para/seu/diretorio
Crie um novo projeto Jekyll:

bash
Copiar código
jekyll new nome-do-blog
Entre na pasta criada:

bash
Copiar código
cd nome-do-blog
Execute o site localmente:

bash
Copiar código
bundle exec jekyll serve
Acesse no navegador: http://localhost:4000.